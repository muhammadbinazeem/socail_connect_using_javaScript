const mongoose = require("mongoose");

const Schema = mongoose.Schema;

const googleSchema = new Schema(
  {
    email: {
      type: String,
      required: true,
      unique: true,
      trim: true,
      minlength: 3,
    },
    name: {
      type: String,
      required: true,
      trim: true,
      minlength: 3,
    },
  },
  {
    timestamps: true,
  }
);

const googles = mongoose.model("googles", googleSchema);

module.exports = googles;
